import pytest

from src.leetcode.summary_ranges_228 import summary_ranges


@pytest.mark.parametrize(
    "nums, expected",
    [
        # LeetCode 기본 예제
        ([0, 1, 2, 4, 5, 7], ["0->2", "4->5", "7"]),
        ([0, 2, 3, 4, 6, 8, 9], ["0", "2->4", "6", "8->9"]),
        # 보강 - 엣지 케이스 (빈 배열, 단일 원소)
        ([], []),
        ([5], ["5"]),
        # 보강 - 엣지 케이스 (전체가 하나의 연속 범위, 전체가 개별 원소)
        ([1, 2, 3, 4], ["1->4"]),
        ([1, 3, 5, 7], ["1", "3", "5", "7"]),
        # 보강 - 예외 케이스 (음수 포함, 음수-양수 경계)
        ([-3, -2, -1, 0, 1], ["-3->1"]),
        ([-2, 0, 1], ["-2", "0->1"]),
        # 보강 - 예외 케이스 (32비트 정수 경계값)
        ([-2147483648, -2147483647], ["-2147483648->-2147483647"]),
        ([2147483647], ["2147483647"]),
        # 보강 - 추가 검증 (2개 원소, 연속 vs 비연속)
        ([1, 2], ["1->2"]),
        ([1, 3], ["1", "3"]),
    ],
)
def test_summary_ranges(nums: list[int], expected: list[str]):
    assert summary_ranges(nums) == expected
