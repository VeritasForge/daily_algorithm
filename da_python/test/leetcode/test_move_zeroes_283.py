import pytest

from src.leetcode.move_zeroes_283 import move_zeroes


@pytest.mark.parametrize(
    "nums, expected",
    [
        # LeetCode 기본 예제
        ([0, 1, 0, 3, 12], [1, 3, 12, 0, 0]),
        ([0], [0]),
        # 보강 - 엣지 케이스 (단일 원소)
        ([1], [1]),
        # 보강 - 엣지 케이스 (모두 0)
        ([0, 0, 0], [0, 0, 0]),
        # 보강 - 엣지 케이스 (0이 없음)
        ([1, 2, 3], [1, 2, 3]),
        # 보강 - 예외 케이스 (0이 맨 앞에 연속)
        ([0, 0, 1, 2], [1, 2, 0, 0]),
        # 보강 - 예외 케이스 (0이 맨 뒤에 연속, 이미 정렬됨)
        ([1, 2, 0, 0], [1, 2, 0, 0]),
        # 보강 - 예외 케이스 (음수 포함, 상대 순서 유지 검증)
        ([-1, 0, -2, 0, 3], [-1, -2, 3, 0, 0]),
        # 보강 - 예외 케이스 (중복값과 0 혼재)
        ([4, 0, 4, 0, 4], [4, 4, 4, 0, 0]),
        # 보강 - 추가 검증 (0이 사이사이 끼어있음)
        ([1, 0, 2, 0, 3, 0, 4], [1, 2, 3, 4, 0, 0, 0]),
        # 보강 - 추가 검증 (제약 경계값 nums[i] = INT_MIN/INT_MAX)
        ([0, -(2**31), 2**31 - 1, 0], [-(2**31), 2**31 - 1, 0, 0]),
    ],
)
def test_move_zeroes(nums: list[int], expected: list[int]) -> None:
    move_zeroes(nums)
    assert nums == expected
