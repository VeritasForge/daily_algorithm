"""
228. Summary Ranges
https://leetcode.com/problems/summary-ranges/

Difficulty: Easy

문제 설명:
    You are given a sorted unique integer array nums.

    A range [a,b] is the set of all integers from a to b (inclusive).

    Return the smallest sorted list of ranges that cover all the numbers
    in the array exactly. That is, each element of nums is covered by
    exactly one of the ranges, and there is no integer x such that x is
    in one of the ranges but not in nums.

    Each range [a,b] in the list should be output as:
    - "a->b" if a != b
    - "a" if a == b

    Example 1:

    Input: nums = [0,1,2,4,5,7]
    Output: ["0->2","4->5","7"]
    Explanation: The ranges are:
    [0,2] --> "0->2"
    [4,5] --> "4->5"
    [7,7] --> "7"

    Example 2:

    Input: nums = [0,2,3,4,6,8,9]
    Output: ["0","2->4","6","8->9"]
    Explanation: The ranges are:
    [0,0] --> "0"
    [2,4] --> "2->4"
    [6,6] --> "6"
    [8,9] --> "8->9"

문제 해석 (원문이 "연속"이라는 단어를 쓰지 않는 이유):
    원문을 한 줄씩 분해하기

    ┌─────────────────────────────────────┬─────────────────────────────┬────────────────────────────────────────────────────────┐
    │              원문 문장              │            직역             │                이게 실제로 의미하는 것                 │
    ├─────────────────────────────────────┼─────────────────────────────┼────────────────────────────────────────────────────────┤
    │ "You are given a sorted unique      │ 정렬되어 있고 중복 없는     │ 이 전제조건이 있어야 "다음 숫자 == 현재 숫자+1"이라는  │
    │ integer array nums."                │ 정수 배열이 주어진다        │ 단순 비교로 연속 여부를 판단할 수 있음 (정렬 안 됐으면 │
    │                                     │                             │  인덱스 비교가 무의미)                                 │
    ├─────────────────────────────────────┼─────────────────────────────┼────────────────────────────────────────────────────────┤
    │ "A range [a,b] is the set of all    │ 범위 [a,b]란 a부터 b까지의  │ 여기 "모든"이 핵심. [0,4]라는 range를 만들면, 이는     │
    │ integers from a to b (inclusive)."  │ 모든 정수 집합이다          │ {0,1,2,3,4} 전부를 뜻하는 것으로 정의됨                │
    ├─────────────────────────────────────┼─────────────────────────────┼────────────────────────────────────────────────────────┤
    │ "Return ... list of ranges that     │ 배열의 모든 숫자를 정확히   │                                                        │
    │ cover all the numbers exactly."     │ 커버하는 range 리스트를     │ "정확히"라는 단어가 다음 문장에서 구체화됨             │
    │                                     │ 반환하라                    │                                                        │
    ├─────────────────────────────────────┼─────────────────────────────┼────────────────────────────────────────────────────────┤
    │ "there is no integer x such that x  │ range 안에는 있는데 nums엔  │                                                        │
    │ is in one of the ranges but not in  │ 없는 정수 x가 존재해서는 안 │ 🔑 여기가 진짜 핵심 문장입니다.                        │
    │ nums."                              │  된다                       │                                                        │
    └─────────────────────────────────────┴─────────────────────────────┴────────────────────────────────────────────────────────┘

    왜 저 마지막 문장이 "연속된 숫자만 묶어라"로 이어지는가

    이 문장을 논리적으로 따라가 보면:

    1. range [a,b]는 정의상 a부터 b까지 모든 정수를 포함합니다 (두 번째 문장)
    2. 그런데 규칙은 "range 안에 있으면서 nums엔 없는 숫자가 있으면 안 된다"고 합니다 (네 번째 문장)
    3. 그렇다면 → a부터 b까지의 모든 정수가 반드시 nums 안에 실제로 존재해야 한다는 뜻이 됩니다
    4. 그런데 nums는 정렬 + 중복 없음이 보장되므로 (첫 문장), "a부터 b까지 빠짐없이 존재한다" = **"a, a+1, a+2, ..., b가 배열에 연속으로 나란히 붙어있다"**와 같은 말이 됩니다

    즉, 아래처럼 반례로 확인해보면 명확해집니다:

    nums = [0, 1, 2, 4, 5, 7]

    만약 range를 [0, 4]로 잡는다면?
    → 정의상 [0,4] = {0, 1, 2, 3, 4}
    → 그런데 3은 nums에 없음
    → "range 안에 있는데 nums엔 없는 숫자(3)가 존재" → 규칙 위반! ❌

    그래서 range는 [0, 2]까지만 가능 (0,1,2 모두 nums에 실존)
    그 다음은 끊어서 새 range [4, 5] 시작

    정리하면

    ┌──────────────────────────────────────────┬─────────────────────────────────────────────────────────────────────────────────┐
    │                원문 표현                 │                               우리말로 "번역"하면                               │
    ├──────────────────────────────────────────┼─────────────────────────────────────────────────────────────────────────────────┤
    │ sorted unique array                      │ 정렬 + 중복없음 → 인덱스로 연속 판단 가능                                       │
    ├──────────────────────────────────────────┼─────────────────────────────────────────────────────────────────────────────────┤
    │ range = all integers from a to b         │ 구간은 빈틈없이 꽉 찬 정수 뭉치                                                 │
    │ inclusive                                │                                                                                 │
    ├──────────────────────────────────────────┼─────────────────────────────────────────────────────────────────────────────────┤
    │ no integer in range but not in nums      │ 구간 안의 모든 정수는 실제 배열에 있어야 함 → 결국 "연속된 숫자끼리만 묶으라"는 │
    │                                          │  뜻                                                                             │
    ├──────────────────────────────────────────┼─────────────────────────────────────────────────────────────────────────────────┤
    │ smallest list                            │ 가능한 한 적은 개수로 (= 연속되는 건 최대한 다 묶으라)                          │
    └──────────────────────────────────────────┴─────────────────────────────────────────────────────────────────────────────────┘

    이 문제뿐 아니라 LeetCode 문제들이 종종 "consecutive(연속)"라는 쉬운 단어 대신 이렇게 집합론적으로 돌려 말하는 경우가 많은데, 그럴 때는 "규칙을 어기는 반례를 하나 만들어보면" (위의 [0,4] 시도처럼) 빠르게 진짜 의미가 드러납니다.
    - "A range [a,b] is the set of all integers from a to b (inclusive)."
      → range [a,b]는 a부터 b까지의 "모든" 정수를 포함한다는 정의.
    - "there is no integer x such that x is in one of the ranges but not
      in nums."
      → range 안에 있는데 nums엔 없는 정수가 있으면 안 됨.
      → 즉 a부터 b까지의 모든 정수가 실제로 nums에 존재해야 한다는 뜻.
    - nums가 sorted + unique이므로, "a부터 b까지 빠짐없이 존재한다"는
      곧 "a, a+1, ..., b가 배열에 연속으로 나란히 붙어있다"는 것과 같다.
    - 예: nums = [0,1,2,4,5,7]에서 range를 [0,4]로 잡으면 [0,4] = {0,1,2,3,4}
      인데 3이 nums에 없으므로 규칙 위반. 그래서 [0,2]까지만 묶고 끊어야 함.
    - 결론: 이 문제는 "배열에서 연속되는 숫자들을 최대한 묶어 구간으로
      표현하라"는 문제이며, "smallest list"는 연속되는 숫자를 최대한
      다 묶으라는 뜻(구간 개수를 최소화).

제약 조건:
    0 <= nums.length <= 20
    -2^31 <= nums[i] <= 2^31 - 1
    All the values of nums are unique.
    nums is sorted in ascending order.
"""


def summary_ranges(nums: list[int]) -> list[str]:
    ranges = []
    i = 0
    while i < len(nums):
        start = nums[i]
        while i + 1 < len(nums) and nums[i + 1] == nums[i] + 1:
            i += 1
        end = nums[i]

        ranges.append(f"{start}" if start == end else f"{start}->{end}")
        i += 1

    return ranges
