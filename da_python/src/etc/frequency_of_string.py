"""
문자열 주파수(Frequency) 계산 (질문 7)

문제 설명:
    소문자 영문자로만 구성된 문자열(a-z)이 주어졌을 때,
    해당 문자를 문자열 s로 변환하기 위해 다음 인코딩 규칙이 사용됩니다.

    인코딩 규칙:
        1) 'a'는 1, 'b'는 2, ..., 'i'는 9 (한 자리 숫자)
        2) 'j'는 10#, 'k'는 11#, ..., 'z'는 26# (두 자리 숫자 + '#')
        3) 문자가 연속 2회 이상 반복되면, 인코딩된 문자 뒤에 괄호로 횟수 표시
           예: 'aa' → '1(2)', 'jjj' → '10#(3)'

    인코딩된 문자열 s가 주어졌을 때, 원래 디코딩된 문자열에서 각 문자의 개수를 구하세요.

입력:
    s: str - 인코딩된 문자열

출력:
    list[int] - 길이 26의 정수 배열 (인덱스 0='a' 개수, 1='b' 개수, ..., 25='z' 개수)

예시:
    "1226#24#" → 'abzx' → [1, 1, 0, ..., 0, 1, 0, 1]
    "1(2)23(3)" → 'aabbccc' → [2, 1, 3, 0, ..., 0]
    "2110#(2)" → 'bajj' → [1, 1, 0, ..., 2, 0, ..., 0]
    "23#(2)24#25#26#23#(3)" → 'wwxyzwww' → [0, ..., 5, 1, 1, 1]

제약 조건:
    - 1 <= len(s) <= 10^5
    - s는 유효한 인코딩 문자열이 보장됨
    - s는 0~9 숫자, '#', '(', ')' 로만 구성
    - 괄호 안 반복 횟수 c: 2 <= c <= 10^4
"""

import string


def solution(s: str) -> list[int]:
    i, result = len(s) - 1, [0] * len(string.ascii_lowercase)

    while i > -1:
        cnt = 1

        if s[i] == ")":
            j, i = i, i - 1
            while s[i] != "(":
                i -= 1

            cnt = int(s[i + 1 : j])
            i -= 1

        if s[i] == "#":
            idx = int(s[i - 2 : i]) - 1
            result[idx] += cnt
            i -= 3
        else:
            idx = int(s[i]) - 1
            result[idx] += cnt
            i -= 1

    return result
